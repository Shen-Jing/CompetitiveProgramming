 private:
    TreeNode* insert_recursive(TreeNode *root, int val)
    {
        if (!root)
            return new TreeNode(val, nullptr, nullptr);

        if (val > root->val)
            root->right = insertIntoBST(root->right, val);
        else
            root->left = insertIntoBST(root->left, val);
        return root;
    }
